function mergeCards(defaults, raw) {
    return defaults.map((d, i) => ({ ...d, ...(raw?.[i] ?? {}) }));
}
function mergeSteps(defaults, raw) {
    return defaults.map((d, i) => ({ ...d, ...(raw?.[i] ?? {}) }));
}
export function isSectionVisible(section) {
    return section?.isVisible !== false;
}
