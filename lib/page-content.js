export function getPageContentDefaults(slug) {
    if (slug === "why-als")
        return DEFAULT_WHY_ALS_CONTENT;
    if (slug === "about")
        return DEFAULT_ABOUT_CONTENT;
    if (slug === "careers")
        return DEFAULT_CAREERS_CONTENT;
    return null;
}
export function mergePageContent(slug, raw) {
    if (slug === "why-als")
        return mergeWhyAlsContent(raw);
    if (slug === "about")
        return mergeAboutContent(raw);
    if (slug === "careers")
        return mergeCareersContent(raw);
    return raw ?? {};
}
